package Project.Class.Class4;

public class Point {
    int x,y;

    public Point(int a, int b) {
        x=a;
        y=b;
    }
    public String toString(){
        return "(" + x + "," + y + ")";
    }

    public int getX(){
        return x;
    }

    public int getY(){
        return y;
    }
}




